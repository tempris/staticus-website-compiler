  end(...args) {
    this.log('end', ...args);
  }

  init(...args) {
    this.log('init', ...args);
  }

  shutdown(...args) {
    this.log('shutdown', ...args);
  }

  setDebug(value) {
    this.isDebugSelf = value;
  }

